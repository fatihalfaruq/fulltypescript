// 2.OPERATOR PERBANDINGAN

// operator perbandingan adalah membandingkan 2 variabel dengan nilai boolean

// Nilai Sama Dengan (==)
// Nilai dan Tipe Data Sama Dengan (===)
// Tidak Sama Dengan (!=)
// Nilai dan Tipe Data Tidak Sama Dengan (!==)
// Lebih Besar Dari (>)
// Lebih Kecil Dari (<)
// Lebih Besar Dari atau Sama Dengan (>=)
// Lebih Kecil Dari atau Sama Dengan (<=)

let angkagenap: number = 2;
let angkaganjil: number = 3;

// sama dengan

let samadengan = angkagenap == angkaganjil;
console.log(samadengan);

// tidak sama dengan

let tidaksamadengan = angkagenap != angkaganjil;
console.log(tidaksamadengan);

// lebih dari

let lebihdari = angkagenap > angkaganjil;
console.log(lebihdari);

// kurang dari

let kurangdari = angkagenap < angkaganjil;
console.log(kurangdari);

